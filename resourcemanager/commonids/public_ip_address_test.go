var _ resourceids.ResourceId = &PublicIPAddressId{}

func TestNewPublicIPAddressID(t *testing.T) {
	id := NewPublicIPAddressID("12345678-1234-9876-4563-123456789012", "example-resource-group", "publicIPAddressesValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.PublicIPAddressesName != "publicIPAddressesValue" {
		t.Fatalf("Expected %q but got %q for Segment 'PublicIPAddressesName'", id.PublicIPAddressesName, "publicIPAddressesValue")
	}
}

func TestFormatPublicIPAddressID(t *testing.T) {
	actual := NewPublicIPAddressID("12345678-1234-9876-4563-123456789012", "example-resource-group", "publicIPAddressesValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Network/publicIPAddresses/publicIPAddressesValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

