var _ resourceids.ResourceId = &ManagedDiskId{}

func TestNewManagedDiskID(t *testing.T) {
	id := NewManagedDiskID("12345678-1234-9876-4563-123456789012", "example-resource-group", "diskValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.DiskName != "diskValue" {
		t.Fatalf("Expected %q but got %q for Segment 'DiskName'", id.DiskName, "diskValue")
	}
}

func TestFormatManagedDiskID(t *testing.T) {
	actual := NewManagedDiskID("12345678-1234-9876-4563-123456789012", "example-resource-group", "diskValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Compute/disks/diskValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

