func TestNewVirtualHubBGPConnectionID(t *testing.T) {
	id := NewVirtualHubBGPConnectionID("12345678-1234-9876-4563-123456789012", "example-resource-group", "hubValue", "connectionValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.HubName != "hubValue" {
		t.Fatalf("Expected %q but got %q for Segment 'HubName'", id.HubName, "hubValue")
	}

	if id.ConnectionName != "connectionValue" {
		t.Fatalf("Expected %q but got %q for Segment 'ConnectionName'", id.ConnectionName, "connectionValue")
	}
}

